          <p>Em relação ao peso argentino as mesmas comparações mostram elevações de 35% e 105% respectivamente. Há especificidades em cada um desses casos, e nosso objetivo neste comentário não é tratar em detalhe a situação dessas economias. Basta dizer que são economias que apresentam fundamentos frágeis. Ambas apresentam, em particular, uma situação de déficits em conta corrente muito grandes e inflação alta e crescente, principalmente no caso da Argentina. No caso da Turquia, as coisas tornam-se mais complicadas por questões geopolíticas, notadamente as ligadas às relações diplomáticas com os Estados Unidos e suas implicações do ponto de vista comercial.</p>
          <p class="in-quotes">O ponto a ser ressaltado, porém, é que essas economias apresentavam fragilidades há bastante tempo. Em economia, em muitas circunstâncias é fácil identificar uma trajetória insustentável.</p>
          <p>Por exemplo, déficits fiscais muito altos levando a um crescimento muito rápido da dívida pública ou déficits em conta corrente muito grandes produzindo um crescimento excessivamente vigoroso do passivo externo líquido. O que é muito mais difícil é identificar quando a crise, que por vezes se afigura inevitável, ocorrerá.</p>
          <p>O que a história econômica mostra é que, em geral, as crises acontecem quando economias emergentes com fragilidades fundamentais importantes se defrontam com condições externas mais desafiadoras. E isso parece ser exatamente o que tem acontecido com a economia mundial nos últimos meses. Para que fique claro, o cenário global ainda é bastante favorável do ponto de vista de crescimento. Após uma trajetória de desaceleração entre 2012 e 2016, houve uma aceleração importante a partir do ano passado, liderada pelas economias desenvolvidas. Neste ano, o que mais chama a atenção é o vigor da economia norte-americana, cujo crescimento se acelerou de 1,6% para 2,2% no ano passado e neste ano deve ficar próximo a 3%, algo que não se via desde meados da década passada.</p>
          <p>O vigor dos EUA neste ano deriva em parte dos fortes estímulos fiscais que resultam da reforma tributária aprovada no final do ano passado e também da aceleração do crescimento dos gastos incluída no orçamento deste ano. O que é mais notável na evolução recente é que essa aceleração acontece em uma etapa incomum do ciclo econômico. Após 9 anos de contínua expansão, a economia dos EUA dá sinais cada vez mais eloquentes de ter alcançado uma situação de baixíssima ociosidade, devendo a taxa de desemprego alcançar neste ano o menor nível dos últimos 50 anos.</p>
          <p>Tendo a inflação (medida pelo núcleo do deflator dos gastos de consumo) já alcançado a meta de 2% nos 12 meses terminados em julho (vindo de 1,4% em agosto de 2017), e considerando as condições já muito apertadas do mercado de trabalho e as possíveis quedas adicionais do desemprego à frente, o principal risco com que se defrontam autoridades monetárias é uma situação de superaquecimento da economia e aceleração indesejável da inflação nos próximos anos. Deve-se dizer, o quadro ainda mostra inflação na meta e uma tendência apenas gradual de aceleração. A política monetária, porém, opera com defasagens longas, de forma que as decisões tomadas hoje terão efeito predominantemente no desempenho econômico de 2020. Não é de se surpreender, portanto, que o Fed tenha se movido gradualmente, à medida que se delineou uma postura fiscal mais expansionista, para indicar um ritmo mais célere de alta neste ano e um nível terminal para a taxa básica de juros mais alto em 2019 e 2020.</p>
          <p>Em resumo, após uma longa recuperação cíclica e com níveis de ociosidade baixos, o compromisso com a meta de inflação requer que o Fed busque assegurar que o crescimento econômico arrefeça gradualmente e convirja para o potencial. A política fiscal, porém, tem atuado em sentido oposto, produzindo forte aceleração do crescimento. Isto requer um esforço maior da política monetária, que precisa produzir condições financeiras mais apertadas para se contrapor ao estímulo fiscal.</p>
          <p>É nesse contexto que devemos compreender os recentes episódios de crise na Turquia e na Argentina. O gradual aperto de condições financeiras gerado pela perspectiva de juros mais altos nos EUA torna mais difíceis as condições de financiamento para economias emergentes. </p>
          <p class="in-quotes">A intensidade dos efeitos dessa mudança sobre as diferentes economias dependerá essencialmente da robustez de seus fundamentos. Passado o período de condições financeiras excepcionalmente benignas que vivemos em 2017, a tendência é que haja maior escrutínio dos investidores, levando a uma maior diferenciação entre os países.</p>
          <p>O ambiente global mais desafiador possivelmente também explica em parte a depreciação do câmbio enfrentada pelo Brasil no período recente. No último mês, a alta do dólar frente ao real chega a 10%, e desde o início do ano tem-se uma variação de 25%. Ao contrário de Argentina e Turquia, temos uma conta corrente muito próxima do equilíbrio, a inflação está baixa e as expectativas bastante bem ancoradas. Há, contudo, um enorme e persistente desequilíbrio fiscal. Nos últimos 4 anos, a dívida pública elevou-se 25 pontos e alcançou 77% do PIB. Trata-se de um nível já muito alto entre as economias emergentes e, mais grave, não há ainda um mínimo de clareza sobre como será feito um ajuste fiscal de 4 a 5 pontos do PIB requerido para evitar que a dívida pública siga crescendo de forma insustentável.</p>
          <p>É pela necessidade imperiosa de aprovar reformas fiscais de vulto, e politicamente complexas, que os mercados acompanham com tanto interesse o desenrolar do processo eleitoral no Brasil. A capacidade e disposição do futuro governo de implementar essas reformas determinarão se o Brasil poderá retomar o crescimento de forma sustentada ou se enfrentará uma recidiva da gravíssima recessão que vivemos entre 2014 e 2016.</p>
          <p>Em resumo, está em curso uma mudança importante do ambiente financeiro global, que tem na sua origem uma alteração das políticas fiscal e monetária nos EUA. Turquia e Argentina são até aqui as economias que mais sofreram. Nesse mundo menos complacente, tudo indica que o Brasil terá menos tempo para aprovar as reformas e corrigir os seus graves desequilíbrios fiscais. Os mercados permanecerão atentos, portanto, aos sinais que a campanha eleitoral possa trazer em relação à capacidade dos candidatos de entender a gravidade do momento e articular a construção do consenso político requerido para aprovar as reformas.</p>

