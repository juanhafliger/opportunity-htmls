    <h3>Risco Legal</h3>
  </div>
  <div class="col-12 p-0">
    <p>É o risco resultante da não-execução de contratos, por falta de documentação suficiente ou incapacidade contratual. Um risco legal adicional é associado com a falta de diligenciamento em alguns mercados. Todas as operações que incorram em risco legal são revisadas pelo departamento jurídico interno e por consultores externos, quando aplicável. O investimento só é efetuado após a documentação legal estar devidamente analisada e aprovada. 
</p>
